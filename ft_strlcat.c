#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	len_dst;

	i = 0;
	while (dst[i] != '\0' && i < dstsize)
		i++;
	if (i == dstsize)
		return (i + ft_strlen(src));
	len_dst = i;
	j = 0;
	while (src[j] != '\0' && i < dstsize - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (len_dst + ft_strlen(src));
}
